	   function SetMarker(position, flag) {

	       //Remove previous Marker.
	       if (flag == 1) {
	           if (marker != null) {
	               // window.alert("sometext");
	               marker.setMap(null);
	               // window.alert("sometext");
	           }
	       }

	       //Set Marker on Map.
	       var data = markers[position];
	       var second_flag = 0;
	       var myLatlng = new google.maps.LatLng(data.lat, data.lng);
	       var contentString = "<table><tr><td align='center'><img width='100' src=" + data.image + " style='padding:4px'></td></tr><tr><td align='center'><i class='fas fa-map-marker-alt'></i><strong>" + "<br>" + data.title + "</strong></td></tr> <tr><td align='center'> " + "<br>" + data.description + " </td></tr></table>";

	       if (position == 0 || position == 12 || position == 16 || position == 18 || position == 21 || position == 29 || position == 35 || position == 42 || position == 44 || position == 46 || position == 47 || position == 48 || position == 83 || /*position == 87 ||*/ position == 91 || position == 96 || position == 99 || position == 100 || position == 101 || position == 102  || position == 104  ) 
		   {
	           icon = "blue";
	           // icon = "/map/download.png";
	           // icon = "http://maps.google.com/mapfiles/ms/icons/" + icon + ".png";
	           icon = "/map/map_icon_blue.png";
	       } else {
	           //icon="green";
	           icon = "/map/map_icon_green.png";
	       }

	       marker = new google.maps.Marker({
	           position: myLatlng,
	           map: map,
	           title: data.title,
	           animation: google.maps.Animation.DROP,
	           // animation: google.maps.Animation.BOUNCE,

	           icon: new google.maps.MarkerImage(icon)
	       });

	       //Create and open InfoWindow.


	       if (flag == 1) {
	           // var infoWindow = new google.maps.InfoWindow();
	           infoWindow.setContent(contentString);
	           infoWindow.open(map, marker);
	           second_flag = 1
	       }
	       attachSecretMessage(marker, contentString);

	       if ((position == 0 || position == 12 || position == 16 || position == 18 || position == 21 || position == 29 || position == 35 || position == 42 || position == 44 || position == 46 || position == 47 || position == 48) || position == 83 || /*position == 87 ||*/ position == 91 || position == 96 || position == 99 || position == 100 || position == 101 || position == 102 || position == 104  && second_flag == 0) {
	           // infoWindow.setContent('<strong>'+data.title+'</strong>');
	           //var infoWindow = new google.maps.InfoWindow();
	           infoWindow.setContent(contentString);
	           infoWindow.open(map, marker);

	       }



	   };




